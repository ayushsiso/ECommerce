
public class Item {
private String itemname;
private int itemid;
private int price;
public String getItemname() {
	return itemname;
}
public void setItemname(String itemname) {
	this.itemname = itemname;
}
public int getItemid() {
	return itemid;
}
public void setItemid(int itemid) {
	this.itemid = itemid;
}
public int getPrice() {
	return price;
}
public void setPrice(int price) {
	this.price = price;
}

}
